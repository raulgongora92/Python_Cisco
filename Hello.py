#multiples argumentos con print
print("La Witsi Witsi Araña" , "subió" , "a su telaraña.")
print("-----------------------------")

#end muestra las 2 lineas en 1 sola seguida
print("Mi nombre es", "Python.", end=" ")
print("Monty Python.")
print("-----------------------------")

#sep cambia el tipo de separación entre argumentos
print("Mi", "nombre", "es", "Monty", "Python.", sep="-")
print("-----------------------------")

#mezclar end y sep
print("Mi", "nombre", "es", sep="_", end="*")
print("Monty", "Python.", sep="*", end="*\n")
print("-----------------------------")


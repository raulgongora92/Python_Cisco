#Función print muestra en pantalla el argumento que s ele introduzca
print()
print("Hisssssss...")
print("-----------------------------")

#caracter  de escape y nueva linea(\n)
print("La Witsi Witsi Araña\nsubió a su telaraña.")
print()
print("Vino la lluvia\ny se la llevó.")
